package Dto;

/**
 *
 * @author HenryRG
 */
public class CecoDTO {
    private String codCeco ="";
    private String nombreCeco = "";
    private String nitCliente = "";
    private String encargadoCeco = "";

    public CecoDTO() {
    }

    /**
     * @return the codCeco
     */
    public String getCodCeco() {
        return codCeco;
    }

    /**
     * @param codCeco the codCeco to set
     */
    public void setCodCeco(String codCeco) {
        this.codCeco = codCeco;
    }

    /**
     * @return the nombreCeco
     */
    public String getNombreCeco() {
        return nombreCeco;
    }

    /**
     * @param nombreCeco the nombreCeco to set
     */
    public void setNombreCeco(String nombreCeco) {
        this.nombreCeco = nombreCeco;
    }

    /**
     * @return the nitCliente
     */
    public String getNitCliente() {
        return nitCliente;
    }

    /**
     * @param nitCliente the nitCliente to set
     */
    public void setNitCliente(String nitCliente) {
        this.nitCliente = nitCliente;
    }

    /**
     * @return the encargadoCeco
     */
    public String getEncargadoCeco() {
        return encargadoCeco;
    }

    /**
     * @param encargadoCeco the encargadoCeco to set
     */
    public void setEncargadoCeco(String encargadoCeco) {
        this.encargadoCeco = encargadoCeco;
    }
   
       
    
}
